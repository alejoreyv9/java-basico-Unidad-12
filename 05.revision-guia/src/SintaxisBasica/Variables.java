package SintaxisBasica;
/* 

JAVA es un lenguaje de tipado estático, lo que quiere decir que al momento de 
declarar una nueva variable, se debe especificar el tipo de la variable.

    Tipo_Variable Nombre_Variable = Valor_Variable ;
    
*/
public class Variables {
    public static void main(String[] args) throws Exception {
        // Ejemplo de una declaración de variable de tipo Entero
        int numero = 10;
        // Para imprimir por consola se usa System.out.println()
        System.out.println(numero);
    }
}